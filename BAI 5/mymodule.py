def find_max(lst):
    """Hàm tìm phần tử lớn nhất trong danh sách."""
    return max(lst)

def find_min(lst):
    """Hàm tìm phần tử nhỏ nhất trong danh sách."""
    return min(lst)

def sort_list(lst):
    """Hàm sắp xếp danh sách theo thứ tự tăng dần."""
    return sorted(lst)

def find_max_index(lst):
    """Hàm tìm vị trí của phần tử lớn nhất trong danh sách."""
    return lst.index(max(lst))

def find_min_index(lst):
    """Hàm tìm vị trí của phần tử nhỏ nhất trong danh sách."""
    return lst.index(min(lst))
